        <h2><a name="autores"></a>Sobre las personas autoras</h2>

        <p>Cualquier persona o grupo de personas sabedoras y que así lo deseen pueden solicitar la creación de un jardín virtual y proponer
            uno o más materiales para ser publicados en “Las cédulas del jardín”, siempre y cuando el o los materiales cumplan con lo
            establecido en estas normas.</p>

        <p>Todos los materiales deberán incluir los datos de autoría de la o las personas que generaron y son responsables de responder
            por el material y que serán identificadas como “las personas autoras”.</p>

        <p>En el caso  de que la persona autora sea menor de edad, será requisito indispensable contar con el consentimiento
            informado y por escrito de sus padres, madres o tutores legales.</p>

        <p>Las personas autoras, siempre deberán manifestar y reconocer en sus materiales, los nombres completos de las comunidades, pueblos,
            grupos originarios, regiones o sabedores que sean los  propietarios, depositarios o transmisores del conocimiento al que hace
            referencia la información. </p>

        <p>Las personas autoras deberán designar cuando menos a una de ellas para que funja como “autor de correspondencia” y
            que será el responsable de enviar por correo electrónico y dar seguimiento a la publicación a través de  la plataforma y mediante el registro
            con un correo electrónico.</p>

        <p>Los autores deberán declarar el jardín virtual al que desean que pertenezca el material o en caso de ser necesario y
            justificable, solicitar la creación de un jardín virtual propio.</p>

        <p>Los autores deberán manifestar por escrito si dan o no su autorización para la traducción del material o otras lenguas
            y/o español o si este únicamente se deberá de mostrar en su lengua original.</p>

        <p>De manera opcioal, los autores que lo deseen, podrán enviar una fotografía y breve reseña que será mostrada en la
            sección de Autores del Jardín y que estará accesible al público visitante.</p>


        <br>
        <h2><a name="jardines"></a>Sobre los jardines virtuales</h2>

        <p>“Las Cédulas del jardín” funciona mediante la creación de Jardines Virtuales en los que aparece la información
            referente a sus especies. Cada jardín, pertenece a un a comunidad, escuela, jardín etnobiológico o grupo de personas,
            constituidas o identificadas en común dentro de un territorio y lengua y que se identificarán como los responsables de
            la información presentada, con las facultades para generarla y responder por ella.</p>

        <p>Cualquier persona, grupo de personas, jardín etnobiológico o comunidad, bajo causa justificada, puede solicitar la creación
            de un jardín virtual dentro de la plataforma “Las Cédulas del Jardín”, para ello, deberá darse de alta en la plataforma
            y posteriormente, desde su buzón, enviar un mensaje al administrador del Jardín Etnobiológico de Oaxaca (JebOax) con el texto
            de solicitud donde se explique la razón de la solicitud.</p>


        <br>
        <h2><a name="contenidos"></a>Sobre los contenidos</h2>

        <p>Las cédulas del jardín acepta contribuciones en forma de texto, imagen, audio y video que, solas o en conjunto,
            transmiten de manera clara y completa, algún aspecto relacionado con la flora, fauna, funga o microbiota que sea parte del patrimonio
            biocultural de algún pueblo de México.</p>

        <p>Las contribuciones deben ser originales, no publicadas  y abordar temas referentes a la interacción entre
            el ser humano y la biodiversidad.</p>

        <p>Es responsabilidad de las personas autoras, informar a todos aquellos que proporcionen conocimientos, información, imágenes,
            audios, videos, o cualquier otro material que se desee publicar o del que se obtenga información,
            de que ese material será publicado en un portal
            de acceso libre y gratuito,  así como  de informarles
            sobre las implicaciones y alcances de su publicación. Deberán
            contar con su autorización
            asegurando que su publicación sea una herramienta de acompañamiento y no de extractivismo y que no
            comprometa la seguridad, privacidad o integridad cultural de los pueblos.
            Este
            proceso debe garantizar, el respeto irrestricto a los sistemas normativos locales, las tradiciones y los
            reglamentos internos de la comunidad, incluyendo su derecho a decidir qué conocimientos pueden ser divulgados y
            bajo qué condiciones.
            A criterio del Comité Editorial, podrá solicitarse la evidencia de dicha autorización como requisito para su publicación.</p>

        <p>Las publicaciones no tienen periodicidad, ya que se van liberando al público a medida que van concluyendo
            su proceso editorial, por lo que pueden ser enviados en cualquier momento del año.</p>

        <p>Los materiales pueden ser generados en español o en lengua originaria y éstos podrán ser traducidos a una o
            más lenguas. En el caso de materiales generados en lengua originaria, éstos deberán ir acompañados de su traducción al español
            para efectos de revisión o deberán ir acompañados de los datos de contacto de un hablante
            de lengua y de español que pueda revisar los materiales con el Comité Editorial.</p>

        <p>Todos los materiales, escritos, audibles, gráficos o visuales deberán cumplir con lo siguiente:</p>
        <ul>
            <li>Deben abordar aspectos culturales y biológicos sobre la interacción entre las poblaciones humanas y la
                flora, fauna, funga o microbiota de México.</li>
            <li>Deben documentar algún aspecto relacionado a especies nativas mexicanas. Excepcionalmente se podrá
                aceptar la documentación de saberes relacionados a especies introducidas y cuando así sea, debe indicarse en el material.</li>
            <li>Deben ser claros y fácilmente comprendidos por cualquier otra persona hablante de la misma lengua en
                la que fueron generados.</li>
            <li>Deben respetar la propiedad de los saberes, mencionando el nombre completo del pueblo, región,
                territorio, municipio, lengua, gremio de sabedores, o sabedores en los que recae el patrimonio narrado.</li>
            <li>Debe nombrar correctamente las fuentes de información, tanto bibliográficas, como de sabedores
                por comunicación personal indicando siempre las fuentes consultadas.</li>
            <li>Deben ser claros en la distinción del alcance de un saber, aclarando si se trata de saberes
                personales, familiares, grupales, comunitarios, gremiales o de un pueblo.</li>
            <li>Deben ser siempre respetuosos de las lenguas, comunidades, gentes y creencias.</li>
            <li>Deben ser estrictos en la asignación de nombres, así como en la descripción de usos, procesos,
                saberes o narrativas.</li>
            <li>Deben ir acompañados de los materiales necesarios que demuestren lo dicho, ya sea en modo fotográfico,
                audiovisual o bibliográfico.</li>
            <li>Deben ser rigurosos en la asignación de nombres científicos.</li>
            <li>Deben ser respetuoso del conocimiento local y nunca denigrarlo ni minimizarlo por debajo de
                cualquier otro conocimiento.</li>
            <li>Deben poder contar con derecho de autor bajo Licencia Pública General GNU, es decir, el material
                debe contar con los derechos para poderse usar, copiar, modificar y redistribuir y no estar bajo ningún otro tipo de
                licenciamiento restrictivo.</li>
        </ul>


        <h2><a name="texto"></a>Sobre los materiales de texto</h2>

        <p>Se aceptan documentos de texto en modo de artículos de difusión, etnografías, monografías o cédulas museográficas.</p>

        <p>Los documentos de texto deberán entregarse en formato de archivo de texto editable en formato .txt, .odt, .doc, .docx.</p>
        <p>Debe incluir un título principal informativo y atractivo de no más de 50 caracteres.</p>
        <p>Debajo del tíulo debe incluir el nombre y apellido(s) de las personas autoras del documento, indicando con asterisco
            cuando menos a un autor de correspondencia cuyo correo electrónico estará accesible al público.</p>
        <p>El cuerpo del documento debe tener un mínimo de 500 palabras y en caso de ser requerido, puede contener
            hasta dos niveles de subtítulos.</p>
        <p>Se recomienda usar párrafos cortos  no mayores a las 200 palabras.</p>
        <p>Se recomienda evitar el uso de tecnicismos que dificulten la lectura o la traducción a otras lenguas.</p>
        <p>Los nombres en lenguas originarias (distintas a la lengua en la que está escrito el documento) deben ir en negritas.</p>
        <p>Los nombres científicos deben ir en letras itálicas y deben estar en el catálogo de nombres científicos
            “Plants of the world online” del Jardín Botánico de Kew (https://powo.science.kew.org/).</p>
        <p>Las referencias bibliográficas o de comunicación personal dentro del cuerpo del texto principal,
            se indican entre paréntesis con los apellidos de los autores y el año de la publicación separados por una coma.
            En caso de haber más de dos autores, se indica el apellido del primero seguido de et al (que quiere decir “y otros” en latín).
            Ejemplo: (Aguirre-Dugua, 2025), (Gámez-Tamariz y Zarazúa, 2000) (Scheinvar et al, 2026).</p>
        <p>Las referencias  van al final del documente bajo los siguientes formatos:</p>
        <ul>
            <li><b>Comunicación personal</b> Apellido-Apellido Nombre. Rol/función/cargo. Comunicación personal, Año. Comunidad de pertenencia (Com. Pers.)*.
                <br>Ejemplo: Felipe Monterrosa Santiago. Regidor de Cultura de Santiago Matatlán. Comunicación Personal, 2025. Santiago Matatlán, Oaxaca Com Pers.
                <div style="font-size:80%; font-style:italic">* En caso de ser factible y que el comunicador esté de acuerdo,
                incluir algún medio o forma de contacto como correo electrónico, teléfono o dónde preguntar por él.</div>
            </li>
            <li><b>Artículos</b>: Autores*,  Año.  Título. Nombre de la Revista Número(volumen): pg-pg. DOI
                <br>Ejemplo: Scheinvar E, Gámez N, Castellanos-Morales G, Aguirre-Planter E y Eguiarte L.2017. Neogene and Pleistoecene history of Agave lechuguilla in the Chihuahuan Desert.  Journal of Biogeography 44(2):322-334. https://doi.org/10.1111/jbi.12851
                <div style="font-size:80%; font-style:italic">* Autores: Se escribe el primer apellido (o los dos apellidos separados por línea media y sin espacio) y luego las siglas del nombre sin punto ni espacios. Por ejemplo: Scheinvar E, Gámez-Tamariz N, Egiarte LE</div>
            </li>
            <li><b>Libros</b>: Autores* Año. Título. Editorial. ISBN.
                <br>Ejemplo: Scheinvar L, Gallegos-Vázquez C, Gamez-Tamariz N y Olalde-Parra G. 2020. Atlas de nopales silvestres mexicanos. Instituto de Biología, UNAM. ISBN 9786073037228
            </li>
            <li><b>Capítulo</b>: Autores* Año. Título del capitulo. En: Apellido N, Apellido-Apellido N y Apellido N Eds. Título del libro. Editorial. Pg-pg. ISBN DOI
                <br>Ejemplo: Vibrans H. 2016. Ethnobotany of Mexicans Weeds. En: Lira R, Casas A y Blancas J Eds. Ethnobotany of Mexico, Interactions of People and Plants in Mesoamerica. Springer, 287:317 ISBN 978-1-4614-6669-7 DOI 10.1007/978-1-4614-6669-7
                <div style="font-size:80%; font-style:italic">* Autores: Se escribe el primer apellido (o los dos apellidos separados por línea media y sin espacio) y luego las siglas del nombre sin punto ni espacios. Por ejemplo: Scheinvar E, Gámez-Tamariz N, Egiarte LE</div>
            </li>
            <li><b>Tesis</b>: Apellido-Apellido Nombre, Año. Título. Nivel. Facultad. Universidad.
                <br>Ejemplo: Silva Montellano JA. 2001. Ecología reproductiva y genética de pobalaciones de Agave lechuguilla en un gradiente latitudinal.Tesis de doctorado, Instituto de Ecología, UNAM.
            </li>
            <li><b>Sitios web</b>: Nombre del portal. Dirección URL. (fecha de consulta).
                <br>Ejemplo: Inaturalist, Taxodium mucrunatum. https://www.inaturalist.org/taxa/49665-Taxodium-mucronatum (consultado en marzo de 2026)
            </li>
        </ul>
        <p>Los documentos de texto deberán ir acompañados de un mínimo de tres imágenes originales relacionadas al tema del documento.</p>
        <p>En caso de presentar traducciones, deberá incluirse la traducción párrafo a párrafo en un mismo documento
            (arriba el original y abajo el párrafo traducido) indicando el nombre oficial y la autonímia de la lengua según el
            catálogo de lenguas del ethnologue (https://www.ethnologue.com/) así como la comunidad de origen.
            Ejemplo: Ombeyiüts, Huave de San Mateo del Mar, San Mateo del Mar Oaxaca.</p>
        <p>En el caso de haber traducciones a lenguas originarias, se recomienda ampliamente incluir un archivo de audio por
            cada párrafo, con la lectura de los textos en lengua.</p>
        <p>No se admiten figuras, imágenes, audios o videos insertos en el cuerpo del texto.</p>

        <h2><a name="imagen"></a>Sobre los materiales de imagen</h2>
        <p>Las imágenes, figuras y fotografías deberán enviarse en archivos independientes de formato  png, jpg o jpeg.</p>
        <p>Se podrán incluir hasta 10 imágenes que demuestren o documenten los aspectos a tratar.</p>
        <p>Las imágenes deben ser claras, fácilmente entendibles y no estar pixeladas.</p>
        <p>Cada imagen debe tener una resolución mínima de 300 pixeles por pulgada y ancho o largo mínimo de 1000 px y
            sus archivos deben pesar menos de 1MB.</p>
        <p>Cada archivo de imagen debe ir acompañada de los siguientes metadatos:
            <ol>
                <li>Título de la imagen</li>
                <li>Autor(es) de la imagen</li>
                <li>Pie de figura o explicación breve de la imagen (máximo 240 caracteres)</li>
                <li>Mínimo de dos palabras clave que describan la imagen</li>
                <li>Fecha de la imagen</li>
                <li>Ubicación de la imagen</li>
            </ol>
        </p>
        <p>Se recomienda revisar las imágenes previamente con el programa libre y gratuito de edición “gimp” (https://www.gimp.org/).</p>


        <h2><a name="audio"></a>Sobre los materiales de audio</h2>

        <p>Se aceptan archivos de audio que demuestren, documenten, ambienten o narren los aspectos a tratar, así como
            para la escucha de la pronunciación de cada uno de los párrafos escritos en lenguas originarias.</p>
        <p>Los archivos de audio deberán enviarse en archivos independientes de formato ogg, mp3 o wav.</p>
        <p>Los audios deben de ser claros y no presentar distorsión ni ruidos indeseados y tener volumen suficiente
            como para su comprensión. Se recomienda estandarizar el volumen a -14 LUFS.</p>
        <p>Cada archivo deberá tener un tamaño máximo de 1MB.</p>
        <p>Salvo cuando los audios sean de la lectura de la pronunciación de cada uno de los párrafos escritos en lengua,
            cada archivo de audio debe ir acompañado de los siguientes metadatos:
            <ol>
                <li>Título del audio</li>
                <li>Autor(es) del audio</li>
                <li>Pie de figura o explicación breve del audio (máximo 240 caracteres)</li>
                <li>Mínimo de dos palabras clave que describan el audio</li>
                <li>Fecha del audio</li>
                <li>Ubicación del audio</li>
            </ol>
        </p>
        <p>Se recomienda revisar los archivos de audio con el programa libre y gratuito de edición de audio “audacity” (https://www.audacityteam.org/)<

        <h2><a name="video"></a>Sobre los materiales de video</h2>

        <p>Se aceptan archivos de video que demuestren, documenten, expliquen o narren los aspectos a tratar, así como para
