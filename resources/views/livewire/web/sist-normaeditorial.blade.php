    <h1>Norma editorial</h1>


    <div class="formato-grande">
        <p><a name="inicio"></a>"Las Cédulas del Jardín” es un portal electrónico de acceso libre y gratuito, editado por las
            Investigadoras e Investigadores por México de la Secihti en el Jardín Etnobiológico de Oaxaca*<sup>1</sup>
            que brinda un espacio para la documentación de los saberes relacionados al patrimonio biocultural
            de los pueblos mediante la publicación de materiales en forma de artículos de difusión, monografías
            etnografías en formato escrito, audiovisual o audible en leguas originarias o en español.
        </p>

        <p>El patrimonio biocultural es el vínculo vivo e inextricable entre la diversidad biológica y la diversidad
            cultural, que se genera, ejerce, protege y hereda por los pueblos, en un territorio y un tiempo específicos.</p>

        <p>No se limita sólo a especies, ecosistemas, usos o artefactos materiales; abarca saberes, quehaceres, lenguas,
            rituales, sistemas productivos, toponimias y cosmovisiones que los pueblos han desarrollado mediante la interacción continua
            con las plantas, hongos, bacterias y animales de su entorno.</p>

        <p>Este patrimonio, es la herencia o legado territorializado de los pueblos, le pertenece a los pueblos
            que lo poseen y transmiten, es de propiedad colectiva y se hereda de generación tras generación mediante su socialización
            y/o enseñanza.</p>

        <p>Documentar el patrimonio desde una perspectiva colectiva y territorial, debe permitir la visibilización de los saberes
            y de los poseedores del conocimiento.  Debe respetar, salvaguardar, revalorar y proteger la integridad identitaria
            de los pueblos y sus territorios y debe evitar la apropiación y el robo por terceros. Es por ello que el
            portal de “Las Cédulas del Jardín” adopta el <a href="">Código de Ética para la
            investigación, la investigación-acción y la colaboración etnocientífica en América Latina</a>, aprobado por la
            Asamblea General de la Sociedad Latinoamericana de Etnobiología en octubre de 2016
            en Popayán, Colombia. El cual tiene como objetivo principal transformar la relación
            tradicionalmente extractivista entre investigadores y comunidades, estableciendo una colaboración basada en el
            respeto, la reciprocidad y la justicia social.</p>

        <p>Documentar implica dar constancia del patrimonio, desde una perspectiva territorial y con reconocimiento a su proceso de gestación,
            visibiliza a los poseedores del conocimiento, permite proteger y perpetuar el bien común, evita la apropiación y el robo por
            terceros, genera conciencia de lo propio y aumenta el conocimiento que tenemos sobre los mismos.</p>

        <p>Gran parte de la transmisión del saber en los pueblos originarios es de manera oral y se genera en sus lenguas originarias. Cuando
            se extingue una lengua, desaparece la memoria, el conocimiento y la manera de conservación de la biodiversidad generada por cientos
            o miles de años, de ahí que damos prioridad a la documentación en y a la traducción a lenguas originarias.</p>

        <div style="font-size:80%;"><big><big>*<sup>1</sup></big></big>Investigadoras e Investigadores por México es una  iniciativa del Gobierno Mexicano,
            coordinada por la Secretaría de Ciencia, Humanidades, Tecnología e Innovación (SECIHTI). Su objetivo es promover el
            desarrollo profesional de humanistas, científicos y tecnólogos asignándolos a proyectos de investigación prioritarios
            en universidades y centros públicos del país.
        </div>
