        <p>El contenido de los videos debe ser claro y no estar pixeleado.</p>
        <p>En el caso de mostrar caras humanas, los autores deben contar con el consentimiento informado de las
            personas que aparecen.</p>
        <p>Los archivos de audio deberán enviarse en archivos independientes en formato mp4.</p>
        <p>Los archivos deberán poder ser incorporados a la plataforma YouTube, desde donde se embeberán dentro del portal.</p>
        <p>Se recomienda una resolución de 1080px (1920 x 1080) o de 720px (1280 x 720) con relación de aspecto 16:9.</p>
        <p>Cada archivo de video debe ir acompañada de los siguientes metadatos:
            <ol>
                <li>Título del video</li>
                <li>Autor(es) del video</li>
                <li>Pie de figura o explicación breve del video (máximo 240 caracteres)</li>
                <li>Mínimo de dos palabras clave que describan el video</li>
