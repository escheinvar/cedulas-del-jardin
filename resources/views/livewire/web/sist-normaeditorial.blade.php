            </ol>
        </p>

        <h2><a name="publicacion"></a>Cómo iniciar una publicación</h2>
        <p>Para iniciar una publicación, los autores deben registrarse en la plataforma y seguir los pasos establecidos
            para el envío de materiales.</p>

        <h2><a name="revision"></a>Sobre la revisión y el proceso editorial</h2>

        <p>Una vez que el autor de correspondencia haya enviado el material al Administrador del Jardín Etnobiológico de
            Oaxaca (JebOax), éste iniciará el proceso de revisión editorial.

        <p>El Comité Editorial, puede considerar la propuesta para determinar la pertinencia de su publicación,
            pudiendo proponer al autor temáticas o aspectos necesarios a cubrir previo a su publicación.

        <p>El Comité Editorial podrá solicitar la revisión de las contribuciones a manera de doble ciego, a personas
            sabedoras del tema, que podrán opinar o proponer cambios al documento.</p>

        <p>El Comité Editorial hará llegar a los autores las observaciones de los revisores por correo electrónico,
            quienes podrán atenderlas o en su caso, justificar su inatención.</p>

        <p>Una vez acordadas las modificaciones al documento, el Comité Editorial procederá a la carga de los materiales
            en la plataforma. Los autores registrados en la plataforma podrán ingresar a revisar el documento cargado y
            aprobar su publicación final.</p>



        <h2><a name="modificacion"></a>Sobre la modificación de la información</h2>
        <p>Salvo las publicaciones asignadas con número DOI (ver abajo), la información contenida en el portal, puede ser editada  por sus autores. Cada publicación, viene acompañada del número de versión a la que corresponde la información.</p>
        <p>Para realizar cualquier corrección, los autores deberán ingresar al sistema y desde el portal, solicitar y justificar al Administrador del jardín, la necesidad de la edición del material. La solicitud será analizada por el Administrador del Jardín quien podrá autorizar o no el inicio de la edición.</p>
        <p>El autor que haya solicitado la edición, deberá contar con la autorización de todos los autores para realizar cualquier cambio a la información presentada.</p>
        <p>Todo cambio generado dentro del sistema, deberá seguir el proceso de verificación y en su caso, revisión como se indica en el apartado “Sobre la revisión”.</p>
        <p>Toda cambio en el sistema, genera un cambio en el número de versión del material público.</p>



        <h2><a name="doi"></a>Sobre la asignación de DOI</h2>
        <p>Todas las publicaciones, podrán contar con un número DOI que vincule su publicación.</p>
        <p>Debido a que la asignación de números DOI se realiza mediante un proveedor externo, los autores que deseen la asignación de un número, deberán pagar la cantidad correspondiente.</p>
        <p>A solicitud de los interesados, el Comité Editorial, podrá asignar beca del 100% del costo del número DOI a los materiales sobresalientes.</p>
        <p>Una vez que se asigna un número DOI a una publicación, ésta ya no puede ser modificada.</p>
